import numpy as np

# --------------------------
# 1. 节点数量
# --------------------------
n_nodes = 4   # 根据题目修改

# --------------------------
# 2. 定义弹簧
# 格式: (节点i, 节点j, 刚度k)
# 注意：Python索引从0开始
# --------------------------
springs = [
    (0, 1, 1),   # 节点1-2，刚度10
    (1, 2, 1),   # 节点2-3
    (1, 2, 1),   # 节点3-4
    (2, 3, 1),    # 额外连接
    (0, 2, 1)
]

# --------------------------
# 3. 初始化整体刚度矩阵K
# --------------------------
K = np.zeros((n_nodes, n_nodes))

# --------------------------
# 4. 组装刚度矩阵
# --------------------------
for i, j, k in springs:
    K[i, i] += k
    K[j, j] += k
    K[i, j] -= k
    K[j, i] -= k

# --------------------------
# 5. 外力向量
# --------------------------
f = np.array([0, 10, 0, 30])  # 节点4受力10N（示例）

# --------------------------
# 6. 边界条件（约束）
# 假设节点1固定（位移=0）
# --------------------------
fixed_dofs = [0]   # 固定节点
free_dofs = list(set(range(n_nodes)) - set(fixed_dofs))

# 提取自由自由度子矩阵
K_reduced = K[np.ix_(free_dofs, free_dofs)]
f_reduced = f[free_dofs]

# --------------------------
# 7. 求解位移
# --------------------------
u = np.zeros(n_nodes)
u[free_dofs] = np.linalg.solve(K_reduced, f_reduced)

# --------------------------
# 8. 输出结果
# --------------------------
print("节点位移：")
for i, val in enumerate(u):
    print(f"u{i+1} = {val:.6f}")